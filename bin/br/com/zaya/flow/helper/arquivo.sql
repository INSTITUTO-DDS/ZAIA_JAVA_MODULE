SELECT
    (
        SELECT
            NOME
        FROM
            TWFELE
        WHERE
            CODPRN = PRN.CODPRN
          AND VERSAO = PRN.VERSAO
          AND IDELEMENTO = TAR.IDELEMENTO
    ) AS NOMETAREFA,
    TAR.IDELEMENTO
FROM
    TGFCAB_TNF T
        INNER JOIN TGFFIN F ON T.NUNOTA = F.NUNOTA
        INNER JOIN TWFITAR TAR ON TAR.IDINSTPRN = T.TNF_IDINSTPRN
        INNER JOIN TWFIPRN PRN ON PRN.IDINSTPRN = T.TNF_IDINSTPRN
WHERE TAR.DHCONCLUSAO IS NULL
  AND TAR.SITUACAOEXEC <> 'F'
  AND F.NUFIN = :NUFIN;